  show(type) {
    const msgs = this.messages[type];
    if (!msgs || msgs.length === 0) return;

    const msg = msgs[Math.floor(Math.random() * msgs.length)];
    const area = document.getElementById('danmaku-area');
    if (!area) return;

    const div = document.createElement('div');
    div.className = 'danmaku-msg';
    div.textContent = msg;

    div.style.top = Math.random() * 80 + 'px';

    const colors = [
      '#10B981', '#fbbf24', '#60a5fa', '#f87171',
      '#e879f9', '#22d3ee', '#a78bfa', '#fb923c',
    ];
    div.style.color = colors[Math.floor(Math.random() * colors.length)];

    const sizes = ['13px', '15px', '17px', '14px'];
    div.style.fontSize = sizes[Math.floor(Math.random() * sizes.length)];

    if (Math.random() < 0.3) {
      div.style.fontWeight = '700';
      div.style.textShadow = '0 0 8px currentColor';
    }

    area.appendChild(div);
    setTimeout(() => div.remove(), 4000);
  },

